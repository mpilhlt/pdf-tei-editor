#!/usr/bin/env node

/**
 * Generate plugin-registry.js from plugin sources.
 *
 * Scans app/src/plugins/*.js to detect:
 *   - Class-based plugins: `export default class XxxPlugin extends Plugin`
 *   - Object-based plugins: `export const plugin = { name: '...' }`
 *
 * Generates:
 *   - app/src/modules/plugin-registry.js  — JSDoc @typedef for getDependency() typing
 *
 * Also validates that every discovered plugin file is imported in app/src/plugins.js
 * and warns about any missing entries.
 */

import { readFileSync, readdirSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, '..');
const pluginsDir = path.join(projectRoot, 'app', 'src', 'plugins');
const pluginsJsPath = path.join(projectRoot, 'app', 'src', 'plugins.js');
const registryPath = path.join(projectRoot, 'app', 'src', 'modules', 'plugin-registry.js');

/**
 * Detect plugin info from a plugin file's source text.
 * @param {string} source
 * @param {string} filename - base filename (e.g. 'xmleditor.js')
 * @returns {{ name: string, isClass: boolean, hasApi: boolean } | null}
 */
function detectPlugin(source, filename) {
  // Class-based: `class XxxPlugin extends Plugin` (with separate export default, or inline)
  const classDecl = source.match(/class\s+(\w+)\s+extends\s+Plugin\b/);
  if (classDecl) {
    const pluginName = deriveNameFromFilename(filename);
    return { name: pluginName, isClass: true, hasApi: false };
  }

  // Object-based: `const plugin = { name: 'xxx' }` or `export const plugin = { name: 'xxx' }`
  const objectNameMatch = source.match(/(?:export\s+)?const\s+plugin\s*=\s*\{[^}]*?name\s*:\s*['"]([^'"]+)['"]/s);
  if (objectNameMatch) {
    const hasApi = /export\s+const\s+api\b|export\s+\{[^}]*\bapi\b/.test(source);
    return { name: objectNameMatch[1], isClass: false, hasApi };
  }

  return null;
}

/**
 * Derive a plugin name from its filename (strip .js, no extra transform).
 * @param {string} filename
 * @returns {string}
 */
function deriveNameFromFilename(filename) {
  return path.basename(filename, '.js');
}

/**
 * Determine the JSDoc type import expression for a plugin entry.
 * @param {string} filename - base filename
 * @param {boolean} isClass
 * @param {boolean} hasApi
 * @returns {string}
 */
function typeExpression(filename, isClass, hasApi) {
  const rel = `../plugins/${filename}`;
  if (isClass) return `import('${rel}').default`;
  if (hasApi) return `import('${rel}').api`;
  return `import('${rel}').plugin`;
}

// Scan plugin files
const pluginFiles = readdirSync(pluginsDir)
  .filter(f => f.endsWith('.js'))
  .sort();

/** @type {Array<{ filename: string, name: string, isClass: boolean, hasApi: boolean }>} */
const discovered = [];

for (const filename of pluginFiles) {
  const source = readFileSync(path.join(pluginsDir, filename), 'utf-8');
  const info = detectPlugin(source, filename);
  if (info) {
    discovered.push({ filename, ...info });
  }
}

// Generate plugin-registry.js
const lines = [
  '/**',
  ' * Plugin API type registry — maps plugin names to their public API types.',
  ' * Used to type getDependency() calls for IDE auto-completion and type checking.',
  ' *',
  ' * AUTO-GENERATED by `node bin/build.js --steps=plugins` — do not edit manually.',
  ' * Re-run the build step after adding or removing plugins.',
  ' *',
  ' * @typedef {object} PluginRegistryTypes',
];

for (const { filename, name, isClass, hasApi } of discovered) {
  lines.push(` * @property {${typeExpression(filename, isClass, hasApi)}} ${name}`);
}

lines.push(' */', '', 'export {};', '');

writeFileSync(registryPath, lines.join('\n'), 'utf-8');
console.log(`Generated ${path.relative(projectRoot, registryPath)} (${discovered.length} plugins)`);

// Validate that all discovered plugins appear in plugins.js
const pluginsJs = readFileSync(pluginsJsPath, 'utf-8');
const missing = discovered.filter(({ filename }) => !pluginsJs.includes(filename));
if (missing.length > 0) {
  console.warn(`\nWARNING: The following plugin files are not imported in plugins.js:`);
  for (const { filename } of missing) {
    console.warn(`  - app/src/plugins/${filename}`);
  }
  console.warn('Add them manually (ordering is intentional).');
} else {
  console.log(`Validated: all ${discovered.length} plugins are imported in plugins.js`);
}
