#!/usr/bin/env node

/**
 * Generate app/src/module-registry.js from annotated source files.
 *
 * Scans app/src/modules/*.js and app/src/ui.js for files that contain a
 * `@registerModule` JSDoc tag and generates a registerModules() function
 * that pre-registers each as a thin plugin object in the PluginManager.
 *
 * Import style is determined from the source:
 *   - Files with `export default` use a default import.
 *   - All others use a namespace import (`import * as name`).
 *
 * The module name is the filename without extension (e.g. `sl-utils`).
 */

import { readFileSync, readdirSync, writeFileSync } from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.join(__dirname, '..');
const modulesDir = path.join(projectRoot, 'app', 'src', 'modules');
const srcDir = path.join(projectRoot, 'app', 'src');
const outputPath = path.join(srcDir, 'module-registry.js');

/**
 * @typedef {{ name: string, relPath: string, isDefault: boolean }} ModuleEntry
 */

/** @type {ModuleEntry[]} */
const discovered = [];

/**
 * Inspect a source file and return a ModuleEntry if it carries @registerModule.
 * @param {string} absPath - Absolute path to the file
 * @param {string} relFromSrc - Path relative to app/src/ (used in import statements)
 * @returns {ModuleEntry | null}
 */
function inspect(absPath, relFromSrc) {
  const source = readFileSync(absPath, 'utf-8');
  if (!source.includes('@registerModule')) return null;
  const name = path.basename(absPath, '.js');
  const isDefault = /^export\s+default\b/m.test(source);
  return { name, relPath: relFromSrc, isDefault };
}

// Scan app/src/modules/*.js
for (const filename of readdirSync(modulesDir).filter(f => f.endsWith('.js') && !f.includes('/')).sort()) {
  const entry = inspect(
    path.join(modulesDir, filename),
    `./modules/${filename}`
  );
  if (entry) discovered.push(entry);
}

// Also scan top-level app/src/*.js (e.g. ui.js)
for (const filename of readdirSync(srcDir).filter(f => f.endsWith('.js') && !f.includes('/')).sort()) {
  // Skip files in subdirectories and the output file itself
  if (filename === 'module-registry.js') continue;
  const entry = inspect(
    path.join(srcDir, filename),
    `./${filename}`
  );
  if (entry) discovered.push(entry);
}

// Sort by name for stable output
discovered.sort((a, b) => a.name.localeCompare(b.name));

// Build import lines
const importLines = discovered.map(({ name, relPath, isDefault }) => {
  const identifier = isDefault ? name : `* as ${name.replace(/-([a-z])/g, (_, c) => c.toUpperCase())}`;
  // For namespace imports, alias to a camelCase identifier derived from the name
  const alias = name.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
  if (isDefault) {
    return `import ${alias} from '${relPath}';`;
  }
  return `import * as ${alias} from '${relPath}';`;
});

// Build module map entries
const mapLines = discovered.map(({ name, isDefault }) => {
  const alias = name.replace(/-([a-z])/g, (_, c) => c.toUpperCase());
  const key = /[^a-zA-Z0-9_$]/.test(name) ? `'${name}'` : `'${name}'`;
  return `    ${key}: ${alias},`;
});

const output = [
  '/**',
  ' * Module registry — pre-registers utility modules as thin plugin objects.',
  ' * Modules tagged with @registerModule in their source are included automatically.',
  ' *',
  ' * AUTO-GENERATED by `node bin/build.js --steps=modules` — do not edit manually.',
  ' * Re-run the build step after adding or removing @registerModule tags.',
  ' */',
  '',
  ...importLines,
  '',
  '/**',
  ' * Register utility modules with the given PluginManager.',
  ' * Must be called before plugin installation so that dependencies are available.',
  ' * @param {import(\'./modules/plugin-manager.js\').default} pluginManager',
  ' */',
  'export function registerModules(pluginManager) {',
  '  /** @type {Record<string, unknown>} */',
  '  const modules = {',
  ...mapLines,
  '  };',
  '  for (const [name, api] of Object.entries(modules)) {',
  '    pluginManager.register({ name, api });',
  '  }',
  '}',
  '',
].join('\n');

writeFileSync(outputPath, output, 'utf-8');
console.log(`Generated ${path.relative(projectRoot, outputPath)} (${discovered.length} modules: ${discovered.map(m => m.name).join(', ')})`);
